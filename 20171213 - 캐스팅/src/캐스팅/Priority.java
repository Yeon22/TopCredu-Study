package 캐스팅;

public class Priority {

	public static void main(String[] args) {
		
		System.out.println("3+2*8-4 = "+(3+2*8-4));
		
		System.out.println("(3+2)*(8-4) = "+((3+2)*(8-4)));
		
		//비트연산		<<  ,  >>
		//10  -> 1010
		//0001  10100
		//10 << 1
		//10 >> 1
		System.out.println("10 << 1 = "+(10<<1));
		System.out.println("10 >> 1 = "+(10>>1));

	}

}
